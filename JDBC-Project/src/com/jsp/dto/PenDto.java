package com.jsp.dto;

public class PenDto {
	private int penid;
	private String brand;
	private String name;
	private int price;
	
	
	public int getPenid() {
		return penid;
	}
	public void setPenid(int penid) {
		this.penid = penid;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
